package ex_12_While_Vs_DoWhile_Loop;

public class Lab092_Dowhile_Realcase {
    public static void main(String[] args) {
        // Do While atleast execute the program atleast 1 time.
        // Web Automation
        int number = 0;
        // 0 --> it can be 0 to 10 :- student or elements
        do {
            System.out.println(number);
            // call function
            // close the excel file, Open the excel file
            number++;

        } while (number < 0);

    }

}
