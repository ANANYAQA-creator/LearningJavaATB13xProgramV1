package ex_10_ForLoop;

public class Lab071_If_Loop {
    public static void main(String[] args) {

        for (int Shruti = 0; Shruti < 18; Shruti++) {
            if (Shruti > 16) {
                System.out.println("Gift New Mobile Phone from PAPA");
            } else {
                System.out.println("Gift cake/pasteries/chocolate from PAPA");
            }
        }
    }
}

