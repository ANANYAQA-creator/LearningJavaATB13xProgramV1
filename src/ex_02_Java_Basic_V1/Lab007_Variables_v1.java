package ex_02_Java_Basic_V1;

public class Lab007_Variables_v1 {

    public static void main(String[] args) {

        byte b = 10 ;
        System.out.println(b);
    }
    /* Syntax of varibale :- data_type variable_name = variable_value
     *
     *  eg- byte b = 10
     *
     *  byte are data_type ,like other datatypes int, float, boolean, char, long
     *  here b= variable_name also known as "Identifier"
     *  here 10 = variable_value also known as "Literal"
     * */
}
